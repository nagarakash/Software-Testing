
// WAJP to sum values of an array

package Module6;

public class P09_ValuesofArray {
    public static void main(String[] args) {
        // Example array
        int[] numbers = {10, 20, 30, 40, 50};

        int sum = 0;

        // Loop to calculate sum
        for (int num : numbers) {
            sum += num;
        }

        System.out.println("Sum of array elements is: " + sum);
    }
}


