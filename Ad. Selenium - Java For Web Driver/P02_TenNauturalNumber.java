package Module6;

public class P02_TenNauturalNumber {
    public static void main(String[] args) {
        int i = 1; // Starting number

        System.out.println("The first 10 natural numbers are:");

        // while loop to print 1 to 10
        while (i <= 10) {
            System.out.println(i);
            i++;
        }
    }
}
